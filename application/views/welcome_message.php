  <h4 class="media-heading"><b>Historia de la Función Electoral</b></h4>
  La creación del Tribunal Supremo Electoral: La estrategia para la estabilización de la Democracia

Se ha definido el período de historia comprendido entre 1925 y 1945 como etapa perdida para la democracia ecuatoriana. En un intento por estabilizar la democracia, la Ley de Elecciones del 18 de agosto de 1945, publicada en el Registro Oficial Nro. 361, en el gobierno de José María Velasco Ibarra, se creó el Tribunal Supremo Electoral, conforme la Constitución de 1945, que por primera vez institucionaliza los organismos electorales con aspiración de independencia y con participación de la expresión política nacional . Adjunto, se crearon los Tribunales Provinciales, Cantonales y Parroquiales con sus funciones. Se regularizaron los partidos políticos desde el punto de vista electoral; se garantizó la propaganda política y la convocatoria a elecciones por medios de comunicación.

El Tribunal Supremo Electoral (TSE), inició su tarea el 11 de marzo de 1947, conformando su estructura nacional mediante la designación de Organismos Electorales Provinciales. También, diseño y ejecutó procedimientos para aplicar la norma legal electoral.
